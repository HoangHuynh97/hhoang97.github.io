<script type="text/javascript">
	$(document).ready(function() {
		Swal.fire({
		  	icon: 'warning',
		  	title: 'Oops...',
		  	text: 'Chưa có bản responsive, vui lòng xem trên PC/LAPTOP!'
		})
		setInterval(() => checkTimeBanner(), 1000);
		setInterval(() => checkTimeBanner2(), 1000);
	});	
	
	var timeOutBanner = 5;
	function checkTimeBanner() {
		timeOutBanner--;
		if(timeOutBanner == 0) {
			clickSlide("next");
		}
	}
	var timeOutBanner2 = 5;
	function checkTimeBanner2() {
		timeOutBanner2--;
		if(timeOutBanner2 == 0) {
			clickSlide2("next");
		}
	}

	$(".btn-prev").on('click', function (e) {
		clickSlide("prev");
	});
	$(".btn-next").on('click', function (e) {
		clickSlide("next");
	});

	$(".action-prev-nth-2").on('click', function (e) {
		clickSlide2("prev");
	});
	$(".action-next-nth-2").on('click', function (e) {
		clickSlide2("next");
	});

	function clickSlide(type) {
		if(type == "next") {
			$('.box-img:nth-child(1)').appendTo('.container-slide-img');
		} else if(type == "prev") {
			$('.box-img:nth-child(5)').prependTo('.container-slide-img');
		}
		var point = $('.box-img:nth-child(2)').attr('data-img');
		if(point == 1) {
			point = 5;
		} else {
			point = point - 1;
		}
		$('.dot-themes').removeClass('active');
		$('.dot-themes:nth-child('+point+')').addClass('active');
		timeOutBanner = 5;
	}

	function clickSlide2(type) {
		if(type == "next") {
			$('.box-img-nth-2:nth-child(1)').appendTo('.container-slide-img-nth-2');
		} else if(type == "prev") {
			$('.box-img-nth-2:nth-child(5)').prependTo('.container-slide-img-nth-2');
		}
		var point = $('.box-img-nth-2:nth-child(2)').attr('data-img');
		if(point == 1) {
			point = 5;
		} else {
			point = point - 1;
		}
		$('.dot-themes-nth-2').removeClass('active');
		$('.dot-themes-nth-2:nth-child('+point+')').addClass('active');
		timeOutBanner2 = 5;
	}
	
	let win_height = screen.height - 100;
	$(function () {
        var $win = $(window);
        $win.scroll(function () {
            if ($win.scrollTop() == 0) {
                $('.content-menu').removeClass('active');
                $('.box-menu').removeClass('active');
				$('.box-menu[data-point="1"]').addClass('active');
            }
            else {
                $('.content-menu').addClass('active');
            }

            var elem_nth_2 = document.querySelector("#line-menu-nth-2");
			var point_nth_2 = elem_nth_2.getBoundingClientRect();
			if(point_nth_2.top <= 250 && point_nth_2.bottom < win_height) {
				$('.box-menu').removeClass('active');
				$('.box-menu[data-point="2"]').addClass('active');
				$('#line-menu-nth-2').addClass('active');
				$('.container-nth-2-left').addClass('active');
				$('.container-nth-2-right').addClass('active');
				
			} else {
				$('#line-menu-nth-2').removeClass('active');
				$('.container-nth-2-left').removeClass('active');
				$('.container-nth-2-right').removeClass('active');
			}

			var elem_nth_3 = document.querySelector("#line-menu-nth-3");
			var point_nth_3 = elem_nth_3.getBoundingClientRect();
			if(point_nth_3.top <= 250 && point_nth_3.bottom < win_height) {
				$('.box-menu').removeClass('active');
				$('.box-menu[data-point="3"]').addClass('active');
				$('#line-menu-nth-3').addClass('active');
				$('.container-box').addClass('in');
				
			} else {
				$('#line-menu-nth-3').removeClass('active');
				$('.container-box').removeClass('in');
			}

			var elem_nth_4 = document.querySelector("#line-menu-nth-4");
			var point_nth_4 = elem_nth_4.getBoundingClientRect();
			if(point_nth_4.top <= 400 && point_nth_4.bottom < win_height) {
				$('.box-menu').removeClass('active');
				$('.box-menu[data-point="4"]').addClass('active');
				$('#line-menu-nth-4').addClass('active');
				$('.container-nth-4').addClass('active');
			} else {
				$('#line-menu-nth-4').removeClass('active');
				$('.container-nth-4').removeClass('active');
			}
        });
    });

